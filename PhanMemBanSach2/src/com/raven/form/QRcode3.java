    private WebcamPanel panel = null;
    private Webcam webcam = null;
    private Executor executor = Executors.newSingleThreadExecutor(this);

    private void initWebcam() {
        List<Webcam> webcams = Webcam.getWebcams();
        if (webcams.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No webcam found!", "Error", JOptionPane.ERROR_MESSAGE);
            dispose();
            return;
        }

        Dimension size = WebcamResolution.QVGA.getSize();
        webcam = webcams.get(0);
        webcam.setViewSize(size);

        panel = new WebcamPanel(webcam);
        panel.setPreferredSize(size);
        panel.setFPSDisplayed(true);

        jPanel2.add(panel, new org.netbeans.lib.awtextra.AbsoluteConstraints(0, 0, 470, 300));
        executor.execute(this);
    }

    public void run() {
        while (running) {
            try {
                Thread.sleep(100);
                if (!webcam.isOpen()) {
                    continue;
                }

                BufferedImage image = webcam.getImage();
                if (image == null) {
                    continue;
                }

                LuminanceSource source = new BufferedImageLuminanceSource(image);
                BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));

                try {
                    Result result = new MultiFormatReader().decode(bitmap);
                    String qrData = result.getText();
                    List<SanPhamCT> qrDataList = getAllQRDataFromDatabase(qrData);
                    if (!qrDataList.isEmpty()) {
                        scannedData = qrDataList;
                        running = false;
                        webcam.close();
                        dispose();
                    } else {
                        System.out.println("Không tìm thấy dữ liệu trong cơ sở dữ liệu cho mã QR: " + qrData);
                    }
                } catch (NotFoundException e) {
                    e.printStackTrace();
                }

            } catch (InterruptedException ex) {
                Logger.getLogger(QRcode3.class.getName()).log(Level.SEVERE, null, ex);
                Thread.currentThread().interrupt();
            }
        }
    }

